    private void resetFields()
    {
        rollNumberTextField.setText("");
        nameTextField.setText("");
        courseComboBox.setSelectedItem("Btech");
        branchNameComboBox.setSelectedItem("CS");
        fatherNameTextField.setText("");
        genderComboBox.setSelectedItem("Male");
    }
    
    private boolean isValidNumber(String num)
    {
        if(num == null || num.isBlank())
            return false;
        
        for(int i=0;i<num.length();i++)
        {
            char ch = num.charAt(i);
            
            if(ch < '0' || ch > '9')
                return false;
        }
        
        return true;
    }
    
    private boolean isValidName(String name)
    {
        if(name == null || name.isBlank())
            return false;
        
       for(char ch : name.toCharArray())
       {
           if(!Character.isLetter(ch) && (ch != ' '))
           {
//               System.out.print(ch + " ");
               return false;
           }
               
       }
        return true;
    }
    
    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String rollno = rollNumberTextField.getText();
        String name = nameTextField.getText();
        String course = (String)courseComboBox.getSelectedItem();
        String branchName = (String)branchNameComboBox.getSelectedItem();
        String fatherName = fatherNameTextField.getText();
        String gender = (String)genderComboBox.getSelectedItem();
        
        
        try{
//           Class.forName("com.mysql.cj.jdbc.Driver"); 
           Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmdb" , "root" , "LamBo5525#");
           Statement st = conn.createStatement();
           ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"'"); //if roll no already exists display msg
           if(rs.next())
           {
               JOptionPane.showMessageDialog(null, "Roll Number Already Exists");
               resetFields();
           }
               
           else
           {
               //check if all input-entries are valid
               if(isValidNumber(rollno) && isValidName(name) && isValidName(fatherName))
               {
                   st.executeUpdate("insert into student values('"+rollno+"' , '"+name+"' , '"+course+"' , '"+branchName+"' , '"+fatherName+"' , '"+gender+"')");
                   JOptionPane.showMessageDialog(null, "Data Added Successfully");
                   setVisible(false);
                   new addNewStudent().setVisible(true);
               }
               else
               {
                   JOptionPane.showMessageDialog(null, "Please Enter Valid Entries");
                   resetFields();
               }
                   
               
           }
           
        }
            
        catch(Exception e){
         JOptionPane.showMessageDialog(null, e.toString());   
        }
        
    }//GEN-LAST:event_saveButtonActionPerformed

