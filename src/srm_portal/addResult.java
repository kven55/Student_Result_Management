    private void resetFields()
    {
        rollNumberField.setText("");
        mathsField.setText("");
        aiField.setText("");
        osField.setText("");
        dbmsField.setText("");
        sadField.setText("");
    }
    
    private boolean isValid(String marks)
    {
        if(marks == null || marks.isBlank() || marks.length() > 2)
            return false;
        
        for(int i=0;i<marks.length();i++)
        {
            char ch = marks.charAt(i);
            if(ch < '0' || ch > '9')
                return false;
        }
        return true;
    }
    
    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String rollno = rollNumberField.getText();
        String maths = mathsField.getText();
        String ai = aiField.getText();
        String os = osField.getText();
        String dbms = dbmsField.getText();
        String sad = sadField.getText();
        
       try{
           //we will only add result if student exists
           Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmdb" , "root" , "LamBo5525#");
           Statement st = conn.createStatement();
           ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"'");
           if(rs.next())
           {
               if(isValid(maths) && isValid(ai) && isValid(os) && isValid(dbms) && isValid(sad))
               {
                   st.executeUpdate("insert into result values("+rollno+" , '"+maths+"' , '"+ai+"' , '"+os+"' , '"+dbms+"' , '"+sad+"')");
                   JOptionPane.showMessageDialog(null , "Result Added Successfully");
                   setVisible(false);
                   new addResult().setVisible(true);
               }
               else
               {
                   JOptionPane.showMessageDialog(null, "Please Enter Valid Credentials");
                   resetFields();
               }
                   
           }
           else
           {
               JOptionPane.showMessageDialog(null , "Student Doesn't Exist");
               resetFields();
           }
               
       }
       
       catch(Exception e){
           JOptionPane.showMessageDialog(null , e.toString());
       }
       

    }//GEN-LAST:event_saveButtonActionPerformed

