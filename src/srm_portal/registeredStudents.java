    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try{
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmdb" , "root" , "LamBo5525#");
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select * from student");

            DefaultTableModel tableModel = new DefaultTableModel();
            ResultSetMetaData metaData = rs.getMetaData(); //to get colmn names and ct
            int colCt = metaData.getColumnCount();

            for(int i=1;i<=colCt;i++)
                tableModel.addColumn(metaData.getColumnName(i)); //for adding column names

            Object[] row = new Object[colCt];
            while(rs.next())
            {
                for(int i=0;i<colCt;i++)
                    row[i] = rs.getObject(i+1);

                tableModel.addRow(row); //adding row data to table
            }

            //add table model to table
            registeredStudentsTable.setModel(tableModel);
            
//            registeredStudentsTable.setModel(DbUtils.resultSetToTableModel(rs));
            registeredStudentsTable.getColumnModel().getColumn(1).setPreferredWidth(120);
            registeredStudentsTable.getColumnModel().getColumn(4).setPreferredWidth(120);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

