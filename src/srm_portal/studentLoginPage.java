    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        String rollno = studentId.getText();
        try{
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmdb" , "root" , "LamBo5525#");
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"'");
            if(rs.next())  //student exists
            {
                rs = st.executeQuery("select * from result where rollno = "+rollno+"");
                if(rs.next()) //result updated for the student
                {
                    setVisible(false);
                    new studentResultPage(rollno).setVisible(true);
                }
                else
                    JOptionPane.showMessageDialog(null, "Result is not Available at the moment"); 
            }
            else
                JOptionPane.showMessageDialog(null, "Incorrect Student Id!!!");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
       
    }//GEN-LAST:event_loginButtonActionPerformed

