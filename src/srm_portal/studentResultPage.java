    private void setFields()
    {
        try{
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmdb" , "root" , "LamBo5525#");
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result on student.rollno = result.rollno and student.rollno = "+rollno+"");
            if(rs.next())
            {
                rollNumberField.setText(rs.getString(1));
                nameField.setText(rs.getString(2));
                courseField.setText(rs.getString(3));
                branchField.setText(rs.getString(4));
                fatherNameField.setText(rs.getString(5));
                genderField.setText(rs.getString(6));
                
                int mathMarks = Integer.parseInt(rs.getString(8));
                int aiMarks = Integer.parseInt(rs.getString(9));
                int osMarks = Integer.parseInt(rs.getString(10));
                int dbmsMarks = Integer.parseInt(rs.getString(11));
                int sadMarks = Integer.parseInt(rs.getString(12));
                
                int total = (mathMarks + aiMarks + osMarks + dbmsMarks + sadMarks);
                
                
                mathsField.setText(rs.getString(8));
                aiField.setText(rs.getString(9));
                osField.setText(rs.getString(10));
                dbmsField.setText(rs.getString(11));
                sadField.setText(rs.getString(12));
                totalField.setText(""+total+" / 500");
                
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }
